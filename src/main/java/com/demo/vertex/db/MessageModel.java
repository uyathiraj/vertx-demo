package com.demo.vertex.db;

public class MessageModel {
	
	private String id;
	
	private String message;
	
	public MessageModel() {
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
}
